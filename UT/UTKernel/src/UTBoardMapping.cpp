// UT maps can be generated from DB in UTReadoutTool.cpp
void UTBoardMapping::ClearUTMap() {
  s_UTSourceIDToNumber.clear();
  s_UTNumberToSourceID.clear();
}

void UTBoardMapping::AddUTMapEntry(unsigned int sourceID, unsigned int number) {
  s_UTSourceIDToNumber[sourceID] = number;
  s_UTNumberToSourceID[number]   = sourceID;
}

std::string UTBoardMapping::printUTMap(){

  std::ostringstream o;
  o << " *** UT board to sourceID Map ***" << std::endl;
  const Map& theMap = UTSourceIDToNumberMap();
  for(Map::value_type board : theMap) {
    o << "SourceID: " << board.first << " Flat number: " << board.second << std::endl;
  } // for each
  o << "**************" << std::endl;
  return o.str();
}
