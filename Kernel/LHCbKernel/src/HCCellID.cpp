std::ostream& LHCb::HCCellID::fillStream ( std::ostream& os ) const
{
  return os << "CellID: "<< this->cellID() 
            <<" | Side: "<< this->side() 
            <<" | Station: " << this->station() 
            << "| Cell: " << this->quadrant();
}

